// 21. Verifica a igualdade entre duas expressões
console.log(4 + 4 == 2 * 4); // Retorna true se as expressões forem iguais

// 22. Avalia uma expressão lógica que retorna false
console.log(5 == 5 && !(3 == 3)); // Retorna false

// 23. Avalia uma expressão lógica que retorna true
console.log(1 !== 2 || 5 !== 5 || 3 == 3 || 0 !== 0); // Retorna true

// 24. Executa uma expressão complexa considerando prioridades de operadores
