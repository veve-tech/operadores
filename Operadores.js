// 10. Utiliza operadores de atribuição composta para realizar operações sucessivas
let a = 20;
let b = 5;
a += b; // Soma b ao valor atual de a
a *= 2; // Multiplica o valor atualizado de a por 2
console.log(a); // Exibe o valor final de a

// 11. Verifica se 10 é maior que 7 (comparação booleana)
console.log(10 > 7); // Retorna true

// 12. Verifica se 5 é menor ou igual a 3
console.log(5 <= 3); // Retorna false

// 13. Compara valores utilizando == (igualdade de valor) e === (igualdade estrita)
console.log(7 == "7"); // Compara apenas o valor (retorna true)
console.log(7 === "7"); // Compara valor e tipo (retorna false)

// 14. Compara valores utilizando !== (diferente estrito) e != (diferente de valor)
console.log(10 !== "10"); // Compara valor e tipo (retorna true)
console.log(10 != "10"); // Compara apenas o valor (retorna false)

// 15. Utiliza o operador lógico E (&&) para avaliar duas expressões booleanas
console.log(5 > 3 && 8 < 10); // Retorna true se ambas forem verdadeiras

// 16. Utiliza o operador lógico OU (||) para avaliar duas expressões booleanas
console.log(5 < 3 || 8 === 8); // Retorna true se pelo menos uma for verdadeira

// 17. Utiliza o operador de negação lógica (!) para inverter o resultado da comparação
console.log(!(7 > 2)); // Retorna false

// 18. Demonstra a diferença de precedência entre expressões com e sem parênteses
console.log((4 + 3) * 2); // Operação com parênteses
console.log(4 + 3 * 2); // Operação respeitando a ordem dos operadores

// 19. Calcula a exponenciação de 2 elevado a 4
console.log(2 ** 4); // Exibe o resultado da potência

// 20. Avalia uma expressão composta com operações de soma, divisão e potência
